package ecommerce.system.api.dto;

public class ProductDetailLabelDTO {

    private int id;
    private String name;

    public ProductDetailLabelDTO(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
